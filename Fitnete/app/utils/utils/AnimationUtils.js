export default {
    getAnimation(name) {
        const [exerciseName, gender] = name.split('-');
        switch (gender) {
            case 'male':
                return getMaleAnimation(exerciseName);
            case 'female':
                return getFemaleAnimation(exerciseName);
            default:
                return getMaleAnimation(exerciseName);
        }
    }
}